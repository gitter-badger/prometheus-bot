package org.prokyon.crypto_currency.bot_core;



public class ExchangeAdapterException extends Exception{


		public ExchangeAdapterException()
        {
        }

		public ExchangeAdapterException(String message)
        {
            super(message);
        }

		public ExchangeAdapterException(Throwable cause)
        {
            super(cause);
        }

		public ExchangeAdapterException(String message, Throwable cause)
        {
            super(message, cause);
        }

		public ExchangeAdapterException(String message, Throwable cause,
        boolean enableSuppression, boolean writableStackTrace)
        {
            super(message, cause, enableSuppression, writableStackTrace);
        }
    }


